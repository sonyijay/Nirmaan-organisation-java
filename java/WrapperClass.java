package day17;

public class WrapperClass {
	public static void main(String[] args) {
		int num =20;
		Integer autoBoxing =num;
		System.out.println(autoBoxing);
		
		int unboxing = autoBoxing;
		System.out.println(unboxing);
		
		byte num1 =123;
		Byte autoBoxing1 =num1;
		System.out.println(autoBoxing1);
		
		byte unboxing1 = autoBoxing1;
		System.out.println(unboxing1);
		
		short num2 =20000;
		Short autoBoxing2 =num2;
		System.out.println(autoBoxing2);
		
		short unboxing2 = autoBoxing2;
		System.out.println(unboxing2);
		
		long num3 =6369753137l;
		Long autoBoxing3 =num3;
		System.out.println(autoBoxing3);
		
		long unboxing3 = autoBoxing3;
		System.out.println(unboxing3);
		
		float num4 =3.77f;
		Float autoBoxing4 =num4;
		System.out.println(autoBoxing4);
		
		float unboxing4 = autoBoxing4;
		System.out.println(unboxing4);
		
		double num5 =99.887689765876d;
		Double autoBoxing5 =99.9768959d;
		System.out.println(autoBoxing5);
		
		double unboxing5 = autoBoxing5;
		System.out.println(unboxing5);
		
		
		
Double num7=99.67d;
System.out.println(num7);
		
		
		
		
	}

}
